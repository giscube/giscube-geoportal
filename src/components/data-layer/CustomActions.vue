<template>
  <div class="custom-actions"
      @click.prevent.stop="">
    <q-btn
      v-show="edited && !isNew"
      flat
      dense
      size="sm"
      icon="undo"
      :disabled="!edited || saving || isNew"
      @click="undoRow(row)"
    />
    <q-btn
      flat
      dense
      size="sm"
      icon="edit"
      :disabled="!editing || saving"
      @click="row.uiEdit()"
    />

  </div>
</template>

<script>
import { QBtn } from 'quasar'
import UndoMixin from './UndoMixin'

export default {
  mixins: [UndoMixin],
  props: {
    row: {
      type: Object,
      required: true
    }
  },
  components: {
    QBtn
  },
  computed: {
    editing () {
      return this.row.parent.editing
    },
    deleted () {
      return this.row.status.deleted
    },
    edited () {
      return this.row.status.edited
    },
    isNew () {
      return this.row.status.new
    },
    saving () {
      return this.row.parent.saving
    }
  }
}
</script>
